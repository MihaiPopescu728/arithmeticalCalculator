package org.fasttrackit;

public class OperationsCalculator {

    public static double computeSum( int number1, int number2) {
        return number1 + number2;
    }

    public static double computeSum (double number1, double number2, double number3) {
        return number1 * number2 * number3;
    }

    public static double computeSum(double number1, double number2) {
        return number1 - number2;
    }
    public static double computeSum (double number1) {
        return number1 / 10;
    }

}
